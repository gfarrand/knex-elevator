#ifndef __BLINKER_TASK_H__
#define __BLINKER_TASK_H__


// /////////////////////////////////////////////////////////////////////////////
// Include(s)
// /////////////////////////////////////////////////////////////////////////////
#include <Arduino_FreeRTOS.h>


////////////////////////////////////////////////////////////////////////////////
/// @brief
/// Abstracts the Smoke Alarm main task.
////////////////////////////////////////////////////////////////////////////////
class BlinkerTask
{
    public:
        ////////////////////////////////////////////////////////////////////////
        /// @brief
        /// Basic constructor
        ////////////////////////////////////////////////////////////////////////
        BlinkerTask(void);

        ////////////////////////////////////////////////////////////////////////
        /// @brief
        /// Basic destructor
        ////////////////////////////////////////////////////////////////////////
        ~BlinkerTask(void);

        ////////////////////////////////////////////////////////////////////////
        /// @brief
        /// Initializes resources required by this task.
        ///
        /// @remarks
        /// To be called from the Arduino setup() method.
        ////////////////////////////////////////////////////////////////////////
        void Initialize(void);


    private:
        ////////////////////////////////////////////////////////////////////////
        /// @brief
        /// Thread entry point.
        ///
        /// @param[in] pvParameters   Task parameters set during task creation.
        ////////////////////////////////////////////////////////////////////////
        static void RunWrapper(void * pvParameters);

        ////////////////////////////////////////////////////////////////////////
        /// @brief
        /// The body of the task - typically an infinite loop.
        ///
        /// @see RunWrapper()
        ////////////////////////////////////////////////////////////////////////
        void Run(void);
};


#endif // __BLINKER_TASK_H__


